<p>A primeira exposição “Grabados e Gravuras” foi realizada em Campinas em 1991 e as demais edições foram apresentadas em mais de 50 mostras em várias cidades como São Paulo, Curitiba, Rio de Janeiro, Brasília, além de Quebec, no Canadá.</p>
