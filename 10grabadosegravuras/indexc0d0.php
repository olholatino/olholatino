<p>A mostra “Recuerdos” de importantes artistas latinos está em exposição de 29 de junho a 27 de julho na Galeria de Arte Thomaz Perina da Academia Campineira de Letras e Artes em Campinas, SP. A curadoria é do prof. Dr. Paulo Cheida Sans.</p>
