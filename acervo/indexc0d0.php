			<description><![CDATA[<div class="feed-description"><div>&nbsp;</div>
<div>É com imensa satisfação que apresentamos o site do Acervo Nacional do Museu Olho Latino, contando com 300 gravuras de 133 artistas, contendo fotos das obras e informações curriculares de seus autores. Apresentamos também o Catálogo Digital da Gravura Brasileira do Acervo Olho Latino, versão 2014, que, certamente, dará impulso para que novas versões aconteçam, conforme a inserção de novas gravuras à coleção. Para ficar mais acessível e com mais facilidade de se ler e apreciar o catálogo, optamos por colocar uma imagem de uma gravura de cada artista e no final a listagem geral das obras.</div>
<div>&nbsp;</div>
<div>Gostaríamos que os artistas que estão com obras de sua autoria no nosso acervo, mantenham contato, enviando novas informações sobre suas exposições e a carreira artística, mantendo-nos atualizados também em relação ao endereço domiciliar e dados de contato. Para isso, o site disponibiliza um e-mail específico sobre o assunto.</div>
<div>&nbsp;</div>
<div>Observamos que colocamos as gravuras dos artistas que estão devidamente inseridos em nossa coleção e, aqueles que, porventura, tenham obras sob os cuidados do Museu Olho Latino, mas não tenham assinado o termo de doação ainda ou não vejam suas obras, entrem em contato para que possamos oficializar a inserção delas em nossa coleção.</div>
<div>&nbsp;</div>
<div>Agradecemos especialmente ao Programa de Ação Cultural – ProAC, da Secretaria da Cultura do Governo do Estado de São Paulo, que muito tem incentivado e valorizado a cultura, pela possibilidade de realização do Projeto Catálogo Digital da Gravura Brasileira do Acervo Olho Latino, contemplado com o seu apoio conforme o Edital nº 25/2013 do Programa de Ação Cultural – “Concurso de Apoio a Projetos de Difusão de Acervos Museológicos no Estado de São Paulo”.</div>
<div>&nbsp;</div>
<div>Desejamos que os apreciadores da arte da gravura adquiram o hábito de visitar os nossos sites que envolvem a atuação do Olho Latino.</div>
<div>&nbsp;</div>
<div>Agradecemos a atenção e venham visitar o setor expositivo do Museu Olho Latino localizado na Al. Lucas Nogueira Garcez, 511 - Parque das Águas – em Atibaia, SP.</div>
